<p>Drugo poribljavanje je bilo 21.03.2001 godine, sa 480 kg kupnog &scaron;arana, naj veći 19,2 kg</p>

<p>Teće poribljavanje se desilo aprila meseca, kada je udruženje donelo 2200kg &scaron;arana iz ribnjaka Ečka i poribila jezero. Tu je bilo i 200kg Amura, prosek riba je bio 3 kg.</p>

<p><a href="http://tresetiste.co.rs/images/articles/2001/14.JPG"><img class="tooBig" src="https://tresetiste.com/mnt/galerija/201803111329.JPG" style="height:150px" /></a> <a href="http://tresetiste.co.rs/images/articles/2001/13.JPG"><img class="tooBig" src="https://tresetiste.com/mnt/galerija/201803111312.JPG" style="height:150px" /></a> <a href="http://tresetiste.co.rs/images/articles/2001/12.JPG"><img class="tooBig" src="https://tresetiste.com/mnt/galerija/201803111302.JPG" style="height:150px" /></a></p>

<p>Četrvrto poribljavanje je obavljeno tokom meseca Oktobra sa 3055kg &scaron;arana i to je bio vrhunac, jer se te godine zav&scaron;etak sezone krunisao Prvim YU Carp Cupom 2001.</p>

<p><a href="http://tresetiste.co.rs/images/articles/2001/15.JPG"><img class="tooBig" src="https://tresetiste.com/mnt/galerija/201803111351.JPG" style="height:150px" /></a> <a href="http://tresetiste.co.rs/images/articles/2001/16.JPG"><img class="tooBig" src="https://tresetiste.com/mnt/galerija/201803111336.JPG" style="height:150px" /></a></p>

<p>Prvi YU Carp Cup 2001 koji se upisao u istoriju &scaron;aranskog sporta održan je bio u Novembru mesecu 2001 gde su mnoge ekipe prvi put uzeli uče&scaron;će u takmičarskom sportu &Scaron;aranskog tipa. Prva tri mesta su zauzele ekipe:</p>

<p>1 mesto - Jagodina(Đura)<br />
2 mesto - UB<br />
