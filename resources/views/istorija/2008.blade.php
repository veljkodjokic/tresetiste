<h2>2008</h2>

<p>&nbsp;</p>

<p>Kao i svake godine pripreme za sezonu 2008 god. bile su podstaknute radovima oko uredjenja obale, i delimicno borbe sa ledenim pokrivacom.Kako bi docekali sezonu spremni bez naknadnih korekcija. Svi ;lanovi kluba su vise nedelja za redom ispunjavali zadatke koji su bili postavljeni tako se posao i mogao zavrsiti do kraja i u roku.</p>

