<div id="article-title"><b>Istorija jezera 2002.</b></div>

<p>Počeli smo sa &scaron;estim poribljavanjem i tako već 22.01.2002 godine pu&scaron;teno je 1888 kg biranih primeraka &scaron;arana. Prosek je bio 6,5kg.</p>

<p>Sedmo poribljavanje je bilo 30.01 2002.godine i 01.02.2002 ukupno pu&scaron;teno za dva dana 1320kg, sa prosekom ribe od 6,80kg. To je bila priprema za velika događanja koja su predhodila za 2002 godine, između ostalog u organizaciji YU CARP-a, veliki turnir u Maju mesecu, pod nazivom TOYOTA YU-OPEN. Koji je privukao pažnju poznatih &scaron;aranđjija iz EVROPE i na&scaron;e Zemlje, koji su svojim uče&scaron;ćem podigli rejting jezera Treseti&scaron;te na nivo koji zaslužuje i gde mu je mesto. A takođe i Palić kao turističkog centra Severa Države. Sa ovim se otvorila sezona na jezeru i ređali su se događaji, između ostalog prvi put u na&scaron;oj drzavi se organizovalo Državno prvenstvo, ba&scaron; tu na jezeru Treseti&scaron;te, koje je ispunjavalo sve uslove, koji su tada bili dati da bi se takmičenja Državnog i Međunarodnog karaktera mogli ba&scaron; tu održavati. Veliku stručnu pomoć smo dobili od strane gsp. Meleg Zorana, koji je svo svoje znanje ustupio da bi se ovaj veliki projekat mogao pokrenuti i na kraju realizovati. Pobednicke ekipe na TOYOTA YU OPEN-u:</p>

